package kr.co.softsoldesk.beans;

public class StadiumBean {
    private int stadium_ID;
    private String name;
    private String location;
    private String tell;
    private String traffic_Information;
    private String stadium_Img;
    private String stadium_SeatImg;
    private String stadium_Info;
    private int team_ID;
    
	public int getStadium_ID() {
		return stadium_ID;
	}
	public void setStadium_ID(int stadium_ID) {
		this.stadium_ID = stadium_ID;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getLocation() {
		return location;
	}
	public void setLocation(String location) {
		this.location = location;
	}
	public String getTell() {
		return tell;
	}
	public void setTell(String tell) {
		this.tell = tell;
	}
	public String getTraffic_Information() {
		return traffic_Information;
	}
	public void setTraffic_Information(String traffic_Information) {
		this.traffic_Information = traffic_Information;
	}
	public String getStadium_Img() {
		return stadium_Img;
	}
	public void setStadium_Img(String stadium_Img) {
		this.stadium_Img = stadium_Img;
	}
	public String getStadium_SeatImg() {
		return stadium_SeatImg;
	}
	public void setStadium_SeatImg(String stadium_SeatImg) {
		this.stadium_SeatImg = stadium_SeatImg;
	}
	public String getStadium_Info() {
		return stadium_Info;
	}
	public void setStadium_Info(String stadium_Info) {
		this.stadium_Info = stadium_Info;
	}
	public int getTeam_ID() {
		return team_ID;
	}
	public void setTeam_ID(int team_ID) {
		this.team_ID = team_ID;
	} 

 
}
